            <h1>Déclaration d'accessibilité</h1>
            <p>Accessibility.com s'engage à fournir un site Web accessible à un public le plus large possible, indépendamment de la technologie ou des capacités physiques. Nous travaillons activement à l'amélioration de l'accessibilité et de la convivialité de notre site Web, et nous nous efforçons ainsi d'être conformes aux nombreuses normes et recommandations.</p>
            <h2>Initiative d'accessibilité du Web</h2>
            <p>Ce site s’efforce de se conformer au niveau A des Règles pour l’accessibilité des contenus Web (WCAG) 1.0 du World Wide Web Consortium (W3C). Ces règles expliquent comment rendre un contenu Web plus accessible aux personnes handicapées. Le respect de ces règles permettra d'améliorer la convivialité du Web pour tous.</p>
            <h2>Normes</h2>
            <p>Nous continuerons à développer le site conformément aux recommandations du World Wide Web Consortium (W3C).
            Ce site s'affiche correctement dans les navigateurs Web récents. Si vous visualisez notre site sur un navigateur plus ancien, il se peut que certaines pages ne s'affichent pas de la manière souhaitée si le navigateur ne respecte pas les normes. Pour utiliser au mieux notre site, nous vous recommandons de mettre à niveau votre navigateur et d'utiliser la dernière version de l'un des navigateurs suivants :
